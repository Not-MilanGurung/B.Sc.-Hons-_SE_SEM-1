#Getting Input
num1 = int(input("Please enter your first number: "))
num2 = int(input("Please enter your second number: "))

#Calculating and printing the outputs
print(f'{num1} + {num2} = {num1 + num2}')
print(f'{num1} - {num2} = {num1 - num2}')
print(f'{num1} * {num2} = {num1 * num2}')
print(f'{num1} / {num2} = {num1 / num2}')